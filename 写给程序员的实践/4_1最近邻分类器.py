#   欧式距离
def get_distance(vector1,vecotr2):

    from math import sqrt
    distance = 0
    for i in range(len(vector1)):
        distance += sqrt((vector1[i] - vecotr2[i])**2)
    return distance


#   推荐列表
def get_Neighbor(itemName,itemVector,items):
    distances =  []
    for otherItem in items:
        if otherItem != itemName:
            distance = get_distance(itemVector,items[otherItem])
            distances.append((distance,otherItem))
    distances.sort()
    return distances

#分类
def classify(user,itemName,itemVector):
    nearest = get_Neighbor(itemName,itemVector,users)[0][1]
    rating = users[user][nearest]
    return rating
		
            
